package day2;

public class AddingTwoNumber {
    public static void main(String[] args) {
        int x = 3;
        int y = 20;
        int sum = x+y;
        System.out.println(x+" + "+y+"="+sum);
        System.out.println(x+" - "+y+"="+(x-y));
        System.out.println(x+" * "+y+"="+(x*y));
        System.out.println(x+" / "+y+"="+(y/x));
        System.out.println(y+" % "+x+"="+(y%x));
    }
}
